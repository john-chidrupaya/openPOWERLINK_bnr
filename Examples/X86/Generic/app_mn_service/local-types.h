#ifndef __INC_local_types_h
#define __INC_local_types_h

#ifndef UINT8
    #define UINT8 unsigned char
#endif

#ifndef UINT16
    #define UINT16 unsigned short int
#endif

#ifndef UINT32
    #define UINT32 unsigned int
#endif

#ifndef UINT64
    #define UINT64 unsigned long long int
#endif

#ifndef INT8
    #define INT8 char
#endif

#ifndef INT16
    #define INT16 short int
#endif

#ifndef INT32
    #define INT32 int
#endif

#ifndef INT64
    #define INT64 long long int
#endif

#endif // __INC_local_types_h
